/**
 * TRACK-96A — Nightly Performance Certification Script
 * 
 * Invoked by daily-pipeline.yml Phase 7.
 * Generates three certification reports from real prediction_registry outcomes:
 *   reports/validation/performance.json
 *   reports/validation/calibration.json
 *   reports/validation/drift.json
 * 
 * Also produces: TRACK-96A_PERFORMANCE_CERTIFICATION.md
 */
import { accuracyEngine } from '../validation/PredictionAccuracyEngine';
import { writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';

const REPORTS_DIR = join(process.cwd(), 'reports', 'validation');
const CERT_PATH = join(REPORTS_DIR, 'TRACK-96A_PERFORMANCE_CERTIFICATION.md');

async function main() {
  console.info('[TRACK-96A] Generating nightly performance certification...');

  mkdirSync(REPORTS_DIR, { recursive: true });

  try {
    const scorecard = await accuracyEngine.getScorecard();

    // Write individual reports
    writeFileSync(
      join(REPORTS_DIR, 'performance.json'),
      JSON.stringify({
        generatedAt: new Date().toISOString(),
        hitRate: scorecard.hitRate,
        alphaRate: scorecard.alphaRate,
        bestFactor: scorecard.bestFactor,
        worstFactor: scorecard.worstFactor,
        perHorizon: scorecard.perHorizon,
        factorRanking: scorecard.factorRanking,
      }, null, 2),
    );

    writeFileSync(
      join(REPORTS_DIR, 'calibration.json'),
      JSON.stringify({
        generatedAt: new Date().toISOString(),
        calibrationError: scorecard.calibration,
        calibrationCurve: scorecard.calibrationCurve,
      }, null, 2),
    );

    writeFileSync(
      join(REPORTS_DIR, 'drift.json'),
      JSON.stringify({
        generatedAt: new Date().toISOString(),
        drift: scorecard.drift,
      }, null, 2),
    );

    // Write certification markdown
    const horizon30 = scorecard.perHorizon?.['30d'];
    const horizon90 = scorecard.perHorizon?.['90d'];
    const horizon365 = scorecard.perHorizon?.['365d'];

    const md = `# TRACK-96A — Prediction Performance Certification

**Generated:** ${new Date().toISOString()}

## Summary

| Metric | Value |
|--------|-------|
| 30-Day Hit Rate | **${scorecard.hitRate}%** |
| Alpha Rate | **${scorecard.alphaRate}%** |
| Best Factor | **${scorecard.bestFactor}** |
| Worst Factor | **${scorecard.worstFactor}** |
| Calibration Error | **${scorecard.calibration}%** |
| Drift Status | **${scorecard.driftStatus}** |

## Horizon Performance

| Horizon | Predictions | Hit Rate | Alpha Rate | Avg Return | Avg Alpha |
|---------|-------------|----------|------------|------------|-----------|
| 30d | ${horizon30?.totalPredictions?.toLocaleString() ?? '—'} | ${horizon30?.hitRate ?? '—'}% | ${horizon30?.alphaRate ?? '—'}% | ${horizon30?.avgReturn ?? '—'}% | ${horizon30?.avgAlpha ?? '—'}% |
| 90d | ${horizon90?.totalPredictions?.toLocaleString() ?? '—'} | ${horizon90?.hitRate ?? '—'}% | ${horizon90?.alphaRate ?? '—'}% | ${horizon90?.avgReturn ?? '—'}% | ${horizon90?.avgAlpha ?? '—'}% |
| 365d | ${horizon365?.totalPredictions?.toLocaleString() ?? '—'} | ${horizon365?.hitRate ?? '—'}% | ${horizon365?.alphaRate ?? '—'}% | ${horizon365?.avgReturn ?? '—'}% | ${horizon365?.avgAlpha ?? '—'}% |

## Classification Performance

| Classification | Samples | Hit Rate | Avg Return |
|----------------|---------|----------|------------|
${(scorecard.classification ?? []).map(c => `| ${c.classification} | ${c.sampleSize.toLocaleString()} | ${c.hitRate}% | ${c.avgReturn}% |`).join('\n')}

## Factor Attribution

| Rank | Factor | Predictive Power | Alpha |
|------|--------|-----------------|-------|
${(scorecard.factorRanking ?? []).map(f => `| #${f.rank} | ${f.factor} | ${f.predictivePower}% | ${f.alphaGeneration > 0 ? '+' : ''}${f.alphaGeneration}% |`).join('\n')}

## Confidence Calibration

| Confidence Bucket | Expected | Actual | Error | Samples |
|-------------------|----------|--------|-------|---------|
${(scorecard.calibrationCurve ?? []).map(c => `| ${c.confidenceBucket} | ${c.expected}% | ${c.actual}% | ±${c.error}% | ${c.sampleSize} |`).join('\n')}

## Drift Monitor

| Metric | Current | Previous | Δ |
|--------|---------|----------|---|
| Hit Rate | ${scorecard.drift?.currentPeriod ?? '—'} | ${scorecard.drift?.previousPeriod ?? '—'} | ${scorecard.drift?.hitRateChange ?? 0}% |
| Alpha | ${scorecard.drift?.currentPeriod ?? '—'} | ${scorecard.drift?.previousPeriod ?? '—'} | ${scorecard.drift?.alphaChange ?? 0}% |

**Status:** ${scorecard.drift?.status?.toUpperCase() ?? '—'}

---

*All metrics sourced from \`prediction_registry\` validated outcomes. No synthetic data.
Generated by TRACK-96A nightly certification pipeline.*
`;

    writeFileSync(CERT_PATH, md);

    console.info('[TRACK-96A] Certification complete.');
    console.info(`  performance.json: ${join(REPORTS_DIR, 'performance.json')}`);
    console.info(`  calibration.json: ${join(REPORTS_DIR, 'calibration.json')}`);
    console.info(`  drift.json: ${join(REPORTS_DIR, 'drift.json')}`);
    console.info(`  certification.md: ${CERT_PATH}`);

  } catch (err: any) {
    console.error('[TRACK-96A] Certification failed:', err.message);
    process.exit(1);
  }
}

main();
