// ── Registry helper functions ─────────────────────────────────────────────

const registryMap: Map<string, FeatureDefinition> = new Map(
  FEATURE_REGISTRY.map((f) => [f.id, f])
);

const registryByFamily: Map<FeatureFamily, FeatureDefinition[]> = (() => {
  const m = new Map<FeatureFamily, FeatureDefinition[]>();
  for (const f of FEATURE_REGISTRY) {
    const arr = m.get(f.family);
    if (arr) arr.push(f);
    else m.set(f.family, [f]);
  }
  return m;
})();

export function getFeatureById(id: string): FeatureDefinition | undefined {
  return registryMap.get(id);
}

export function getFeaturesByFamily(family: FeatureFamily): FeatureDefinition[] {
  return registryByFamily.get(family) ?? [];
}

export function getActiveFeatures(): FeatureDefinition[] {
  return FEATURE_REGISTRY.filter((f) => f.activationStatus === 'active');
}

export function getUnavailableFeatures(): FeatureDefinition[] {
  return FEATURE_REGISTRY.filter(
    (f) => f.activationStatus === 'unavailable' || f.activationStatus === 'experimental'
  );
}

export function getRequiredFeatures(): FeatureDefinition[] {
  return FEATURE_REGISTRY.filter((f) => f.required);
}

export function getFeatureCount(): number {
  return FEATURE_REGISTRY.length;
}

export function getActiveFeatureCount(): number {
  return getActiveFeatures().length;
}

export function getFeatureFamilies(): FeatureFamily[] {
  return Array.from(registryByFamily.keys());
}
