#!/bin/bash
#=============================================================================
# Token Renewal System — StockEX India
# Runs every 12 hours to check & renew all API tokens
# 
# Installation:
#   crontab -e
#   0 */12 * * * /bin/bash /path/to/scripts/token-renewal-system.sh
#
# This script:
#   1. Checks Upstox Analytics Token expiry
#   2. Verifies IndianAPI key is configured
#   3. Saves all tokens to .env files
#   4. Generates Vercel/Render environment config
#   5. Reports status
#=============================================================================

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_DIR="$(dirname "$SCRIPT_DIR")"
LOG_FILE="$PROJECT_DIR/logs/token-renewal.log"

mkdir -p "$PROJECT_DIR/logs"

log() {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $1" | tee -a "$LOG_FILE"
}

log "=== Token Renewal System ==="
log "Project: $PROJECT_DIR"

# Load current .env
if [ -f "$PROJECT_DIR/.env" ]; then
    source "$PROJECT_DIR/.env"
    log "Loaded .env file"
fi

# ── 1. Check Upstox Analytics Token ─────────────────────────────────
log ""
log "--- Upstox Analytics Token ---"
if [ -n "$UPSTOX_ACCESS_TOKEN" ]; then
    # Decode JWT payload to check expiry
    PAYLOAD=$(echo "$UPSTOX_ACCESS_TOKEN" | cut -d'.' -f2 2>/dev/null)
    if [ -n "$PAYLOAD" ]; then
        # Add padding if needed
        case $((${#PAYLOAD} % 4)) in
            2) PAYLOAD="${PAYLOAD}==" ;;
            3) PAYLOAD="${PAYLOAD}=" ;;
        esac
        EXP=$(echo "$PAYLOAD" | base64 -d 2>/dev/null | python3 -c "import sys,json; print(json.load(sys.stdin).get('exp',0))" 2>/dev/null)
        if [ -n "$EXP" ] && [ "$EXP" -gt 0 ]; then
            NOW=$(date +%s)
            DAYS_LEFT=$(( ($EXP - $NOW) / 86400 ))
            log "Token expires in $DAYS_LEFT days"
            if [ "$DAYS_LEFT" -lt 30 ]; then
                log "⚠️  WARNING: Token expires in $DAYS_LEFT days!"
                log "Renew at: https://account.upstox.com/developer/apps"
            else
                log "✅ Token valid"
            fi
        fi
    fi
    # Verify token against API
    STATUS=$(curl -s -o /dev/null -w "%{http_code}" \
        -H "Accept: application/json" \
        -H "Authorization: Bearer $UPSTOX_ACCESS_TOKEN" \
        --max-time 10 \
        "https://api.upstox.com/v2/market/status" 2>/dev/null)
    log "API Status: HTTP $STATUS"
else
    log "❌ No UPSTOX_ACCESS_TOKEN found"
fi

# ── 2. Check IndianAPI Key ──────────────────────────────────────────
log ""
log "--- IndianAPI ---"
if [ -n "$INDIANAPI_KEY" ]; then
    KEY_LEN=${#INDIANAPI_KEY}
    log "✅ IndianAPI key configured (${KEY_LEN} chars)"
else
    log "❌ No INDIANAPI_KEY found"
fi

# ── 3. Check for Algo Trading Token ─────────────────────────────────
log ""
log "--- Upstox Algo Trading ---"
if [ -n "$UPSTOX_ALGO_TOKEN" ]; then
    log "✅ Algo Trading token configured"
else
    log "ℹ️  No Algo Trading token - create at https://account.upstox.com/developer/apps#algo"
fi

# ── 4. Check for Sandbox Token ──────────────────────────────────────
log ""
log "--- Upstox Sandbox ---"
if [ -n "$UPSTOX_SANDBOX_TOKEN" ]; then
    log "✅ Sandbox token configured"
else
    log "ℹ️  No Sandbox token - create at https://account.upstox.com/developer/apps#sandbox"
fi

# ── 5. Generate Vercel environment config ───────────────────────────
log ""
log "--- Generating Vercel Config ---"
VERCEL_ENV_FILE="$PROJECT_DIR/.vercel.env"
cat > "$VERCEL_ENV_FILE" << VERCELEOF
# Generated by token-renewal-system.sh on $(date)
# Add these to Vercel: vercel env add KEY_NAME
UPSTOX_ACCESS_TOKEN=$UPSTOX_ACCESS_TOKEN
UPSTOX_ALGO_TOKEN=$UPSTOX_ALGO_TOKEN
UPSTOX_SANDBOX_TOKEN=$UPSTOX_SANDBOX_TOKEN
INDIANAPI_KEY=$INDIANAPI_KEY
VERCELEOF
chmod 600 "$VERCEL_ENV_FILE"
log "✅ Vercel env file generated: $VERCEL_ENV_FILE"

# ── 6. Generate Render environment config ───────────────────────────
log ""
log "--- Generating Render Config ---"
RENDER_ENV_FILE="$PROJECT_DIR/.render.env"
cat > "$RENDER_ENV_FILE" << RENDEREOF
# Generated by token-renewal-system.sh on $(date)
# Add these to Render dashboard
UPSTOX_ACCESS_TOKEN=$UPSTOX_ACCESS_TOKEN
UPSTOX_ALGO_TOKEN=$UPSTOX_ALGO_TOKEN
UPSTOX_SANDBOX_TOKEN=$UPSTOX_SANDBOX_TOKEN
INDIANAPI_KEY=$INDIANAPI_KEY
RENDEREOF
chmod 600 "$RENDER_ENV_FILE"
log "✅ Render env file generated: $RENDER_ENV_FILE"

# ── 7. Save snapshot to .env.backup ─────────────────────────────────
cp "$PROJECT_DIR/.env" "$PROJECT_DIR/.env.backup"
log "✅ .env backed up to .env.backup"

log ""
log "=== Token Renewal Complete ==="
log "Next run: +12 hours"
