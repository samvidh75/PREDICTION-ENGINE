const temporalValidatorCode = `/**
 * TemporalIntegrityValidator — SSI V3
 * Blocks future-dated factors from prediction generation.
 * Generated by TRACK-59 Agent A.
 */
export interface TemporalViolation {
  source: string;
  symbol: string;
  factorDate: string;
  predictionDate: string;
  delta: number; // days of look-ahead
  severity: 'BLOCK' | 'WARN';
}

export class TemporalIntegrityValidator {
  /**
   * Validates that factor_date <= prediction_date for all factor sources.
   * Returns violations array. If any violation has severity=BLOCK, prediction fails.
   */
  static validateFactors<T extends { symbol: string; factorDate: string }>(
    factors: T[],
    predictionDate: string
  ): { valid: boolean; violations: TemporalViolation[]; report: string } {
    const violations: TemporalViolation[] = [];
    const predDate = new Date(predictionDate);

    for (const factor of factors) {
      const factorDate = new Date(factor.factorDate);
      if (isNaN(factorDate.getTime())) {
        violations.push({
          source: 'factor_snapshot',
          symbol: factor.symbol,
          factorDate: factor.factorDate,
          predictionDate,
          delta: 0,
          severity: 'WARN'
        });
        continue;
      }

      if (factorDate > predDate) {
        const deltaDays = Math.floor((factorDate.getTime() - predDate.getTime()) / 86400000);
        violations.push({
          source: 'factor_snapshot',
          symbol: factor.symbol,
          factorDate: factor.factorDate,
          predictionDate,
          delta: deltaDays,
          severity: deltaDays > 7 ? 'BLOCK' : 'WARN'
        });
      }
    }

    return {
      valid: !violations.some(v => v.severity === 'BLOCK'),
      violations,
      report: \`Temporal Integrity: \${violations.length} violations (\${violations.filter(v => v.severity === 'BLOCK').length} BLOCK, \${violations.filter(v => v.severity === 'WARN').length} WARN)\`
    };
  }

  /**
   * Validates quality data is not future-dated.
   */
  static validateQualityData(
    qualityDate: string | null,
    predictionDate: string
  ): { valid: boolean; blocked: boolean } {
    if (!qualityDate) return { valid: true, blocked: false };
    const qd = new Date(qualityDate);
    const pd = new Date(predictionDate);
    if (isNaN(qd.getTime()) || isNaN(pd.getTime())) return { valid: true, blocked: false };

    return {
      valid: qd <= pd,
      blocked: qd > pd
    };
  }
}
`;

