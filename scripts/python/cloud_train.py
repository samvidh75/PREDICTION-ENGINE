#!/usr/bin/env python3
"""
cloud_train.py — CodeEX Cloud LoRA Fine-Tuning Launcher.

Orchestrates parameter-efficient fine-tuning (PEFT via LoRA) of
Qwen2.5-0.5B-Instruct on a remote cloud GPU node (AWS P4/P5 or Lambda Labs).
Outputs a transformers-compatible adapter folder for GGUF compilation.

Usage:
    # Local preview
    python3 scripts/python/cloud_train.py --dry-run

    # On cloud GPU instance
    python3 scripts/python/cloud_train.py \
        --dataset cloud_agent_dataset.jsonl \
        --output-dir ./codeex-lora-adapter \
        --epochs 3 \
        --lr 2e-4
"""

import argparse
import json
import os
import sys
import subprocess

REQUIRED_ENV_VARS = ["CUDA_VISIBLE_DEVICES", "WANDB_API_KEY"]
RECOMMENDED_IMAGE = "huggingface/transformers-pytorch-gpu:latest"

TRAINING_SCRIPT_TEMPLATE = """#!/usr/bin/env python3
# Auto-generated by cloud_train.py — do not edit manually

import json
import torch
from datasets import Dataset
from transformers import (
    AutoTokenizer,
    AutoModelForCausalLM,
    TrainingArguments,
    Trainer,
    DataCollatorForSeq2Seq,
)
from peft import LoraConfig, get_peft_model, TaskType

DATASET_PATH = "{dataset_path}"
OUTPUT_DIR = "{output_dir}"
BASE_MODEL = "Qwen/Qwen2.5-0.5B-Instruct"
EPOCHS = {epochs}
LR = {lr}
RANK = {rank}
ALPHA = {alpha}
TARGET_MODULES = {target_modules}

def load_chatml_dataset(path):
    rows = []
    with open(path, "r") as f:
        for line in f:
            rows.append(json.loads(line.strip()))
    texts = []
    for row in rows:
        messages = row["messages"]
        parts = []
        for msg in messages:
            role = msg["role"]
            content = msg.get("content", "") or ""
            if role == "system":
                parts.append(f"<|system|>\\n{{content}}\\n")
            elif role == "user":
                parts.append(f"<|user|>\\n{{content}}\\n")
            elif role == "assistant":
                if "tool_calls" in msg:
                    tool_json = json.dumps(msg["tool_calls"])
                    parts.append(f"<|assistant|>\\n{{tool_json}}\\n")
                else:
                    parts.append(f"<|assistant|>\\n{{content}}\\n")
            elif role == "tool":
                parts.append(f"<|tool|>\\n{{content}}\\n")
        parts.append("<|end|>")
        texts.append("".join(parts))
    return Dataset.from_dict({{"text": texts}})

def tokenize_fn(examples, tokenizer):
    return tokenizer(examples["text"], truncation=True, padding="max_length", max_length=2048)

def main():
    tokenizer = AutoTokenizer.from_pretrained(BASE_MODEL, trust_remote_code=True)
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    model = AutoModelForCausalLM.from_pretrained(
        BASE_MODEL,
        torch_dtype=torch.float16,
        device_map="auto",
        trust_remote_code=True,
    )

    lora_config = LoraConfig(
        r=RANK,
        lora_alpha=ALPHA,
        target_modules=TARGET_MODULES,
        lora_dropout=0.1,
        bias="none",
        task_type=TaskType.CAUSAL_LM,
    )
    model = get_peft_model(model, lora_config)
    model.print_trainable_parameters()

    dataset = load_chatml_dataset(DATASET_PATH)
    tokenized = dataset.map(lambda x: tokenize_fn(x, tokenizer), batched=True)

    training_args = TrainingArguments(
        output_dir=OUTPUT_DIR,
        num_train_epochs=EPOCHS,
        per_device_train_batch_size=4,
        gradient_accumulation_steps=4,
        learning_rate=LR,
        fp16=True,
        logging_steps=10,
        save_strategy="epoch",
        report_to="none",
        remove_unused_columns=False,
    )

    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=tokenized,
        data_collator=DataCollatorForSeq2Seq(tokenizer, padding=True),
    )

    trainer.train()
    trainer.save_model(OUTPUT_DIR)
    tokenizer.save_pretrained(OUTPUT_DIR)
    print(f"LoRA adapter saved to {{OUTPUT_DIR}}")

if __name__ == "__main__":
    main()
"""


def main():
    parser = argparse.ArgumentParser(description="CodeEX Cloud LoRA Fine-Tuning Launcher")
    parser.add_argument("--dataset", default="cloud_agent_dataset.jsonl",
                        help="Path to the ChatML JSONL training dataset")
    parser.add_argument("--output-dir", default="./codeex-lora-adapter",
                        help="Output directory for the LoRA adapter weights")
    parser.add_argument("--epochs", type=int, default=3, help="Number of training epochs")
    parser.add_argument("--lr", type=float, default=2e-4, help="Learning rate")
    parser.add_argument("--rank", type=int, default=16, help="LoRA rank (r)")
    parser.add_argument("--alpha", type=int, default=32, help="LoRA alpha")
    parser.add_argument("--target-modules", nargs="+",
                        default=["q_proj", "k_proj", "v_proj", "o_proj"],
                        help="Transformer modules to apply LoRA to")
    parser.add_argument("--dry-run", action="store_true",
                        help="Print the training script without executing")
    parser.add_argument("--launch-cloud", action="store_true",
                        help="Emit cloud instance launch command (AWS/Lambda Labs)")
    args = parser.parse_args()

    if not os.path.exists(args.dataset):
        print(json.dumps({
            "error": f"Dataset not found: {args.dataset}",
            "hint": "Run compile_agent_dataset.py first to generate cloud_agent_dataset.jsonl"
        }))
        sys.exit(1)

    train_script = TRAINING_SCRIPT_TEMPLATE.format(
        dataset_path=os.path.abspath(args.dataset),
        output_dir=os.path.abspath(args.output_dir),
        epochs=args.epochs,
        lr=args.lr,
        rank=args.rank,
        alpha=args.alpha,
        target_modules=json.dumps(args.target_modules),
    )

    train_script_path = os.path.join(os.path.dirname(__file__), "run_codeex_train.py")
    with open(train_script_path, "w") as f:
        f.write(train_script)
    os.chmod(train_script_path, 0o755)

    if args.dry_run:
        print("=== Dry Run: Training Script ===")
        print(train_script)
        print(f"\nScript written to: {train_script_path}")
        print("\nTo launch on a cloud GPU instance:")
        print(f"  python3 {train_script_path}")
        return

    print(json.dumps({
        "status": "training_script_generated",
        "script": train_script_path,
        "dataset": os.path.abspath(args.dataset),
        "output_dir": os.path.abspath(args.output_dir),
        "hyperparameters": {
            "epochs": args.epochs,
            "learning_rate": args.lr,
            "lora_rank": args.rank,
            "lora_alpha": args.alpha,
            "target_modules": args.target_modules,
        },
        "next": f"Upload {train_script_path} and {args.dataset} to cloud GPU, then run: python3 run_codeex_train.py"
    }))


if __name__ == "__main__":
    main()
